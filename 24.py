
# python program to convert decimal to binart,octal and hexadecimal

num=int(input('ENTER THE DECIMAL NUMBER: '))

result=bin(num)
print(result)

# decimal to hexadecimal

num=int(input('Enter the decimal number: '))

result=hex(num)
print(result)

# decimal to octal

num=int(input('Enter the number: '))
result=oct(num)
print(result)